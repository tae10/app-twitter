package data;

public class TweetAttach {
	int idx;
	String uri;
	String tweetCode;
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getUri() {
		return uri;
	}
	public void setUri(String uri) {
		this.uri = uri;
	}
	public String getTweetCode() {
		return tweetCode;
	}
	public void setTweetCode(String tweetCode) {
		this.tweetCode = tweetCode;
	}
	

}
